dynamic getMaxWidthFromTailwindClass(String maxWidthClass) {
    switch (maxWidthClass) {
      case 'max-w-0':
        return 0;
      case 'max-w-none':
        return double.infinity; // none
      case 'max-w-xs':
        return 320; // 20rem to px
      case 'max-w-sm':
        return 384; // 24rem to px
      case 'max-w-md':
        return 448; // 28rem to px
      case 'max-w-lg':
        return 512; // 32rem to px
      case 'max-w-xl':
        return 576; // 36rem to px
      case 'max-w-2xl':
        return 672; // 42rem to px
      case 'max-w-3xl':
        return 768; // 48rem to px
      case 'max-w-4xl':
        return 896; // 56rem to px
      case 'max-w-5xl':
        return 1024; // 64rem to px
      case 'max-w-6xl':
        return 1152; // 72rem to px
      case 'max-w-7xl':
        return 1280; // 80rem to px
      case 'max-w-full':
        return double.infinity; // 100%
      case 'max-w-min':
        return double.minPositive; // min-content
      case 'max-w-max':
        return double.maxFinite; // max-content
      default:
        return null; // Return -1 for unsupported values
    }
  }

